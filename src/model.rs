const ROUNDING_BIAS: i32 = 64; // 128 / 2

/// Build the skill safety classifier model.
///
/// Input [1,35]: normalized skill features (see SkillFeatures::to_normalized_vec)
/// Output [1,4]: [safe_score, caution_score, dangerous_score, malicious_score]
///
/// Uses rounding division: (matmul_result + 64) / 128 instead of matmul_result / 128
pub fn skill_safety_model() -> Model {
    const SCALE: i32 = 7;
    let mut b = ModelBuilder::new(SCALE);

    let input = b.input(vec![1, 35], 1);

    // Create rounding bias constants for each layer
    let rounding_bias_56_data = vec![ROUNDING_BIAS; 56];
    let mut rb56 = Tensor::new(Some(&rounding_bias_56_data), &[1, 56]).unwrap();
    rb56.set_scale(SCALE);
    let rb56_const = b.const_tensor(rb56, vec![1, 56], 1);

    let rounding_bias_40_data = vec![ROUNDING_BIAS; 40];
    let mut rb40 = Tensor::new(Some(&rounding_bias_40_data), &[1, 40]).unwrap();
    rb40.set_scale(SCALE);
    let rb40_const = b.const_tensor(rb40, vec![1, 40], 1);

    let rounding_bias_4_data = vec![ROUNDING_BIAS; 4];
    let mut rb4 = Tensor::new(Some(&rounding_bias_4_data), &[1, 4]).unwrap();
    rb4.set_scale(SCALE);
    let rb4_const = b.const_tensor(rb4, vec![1, 4], 1);

    // Layer 1: [1,35] x [56,35] -> [1,56]
    let mut w1 = Tensor::new(Some(W1), &[56, 35]).unwrap();
    w1.set_scale(SCALE);
    let w1_const = b.const_tensor(w1, vec![56, 35], 1);

    let mm1 = b.matmult(input, w1_const, vec![1, 56], 1);
    // Rounding division: (mm1 + 64) / 128
    let mm1_rounded = b.poly(PolyOp::Add, mm1, rb56_const, vec![1, 56], 1);
    let mm1_rescaled = b.div(128, mm1_rounded, vec![1, 56], 1);

    let mut b1 = Tensor::new(Some(B1), &[1, 56]).unwrap();
    b1.set_scale(SCALE);
    let b1_const = b.const_tensor(b1, vec![1, 56], 1);
    let biased1 = b.poly(PolyOp::Add, mm1_rescaled, b1_const, vec![1, 56], 1);

    let relu1 = b.relu(biased1, vec![1, 56], 1);

    // Layer 2: [1,56] x [40,56] -> [1,40]
    let mut w2 = Tensor::new(Some(W2), &[40, 56]).unwrap();
    w2.set_scale(SCALE);
    let w2_const = b.const_tensor(w2, vec![40, 56], 1);

    let mm2 = b.matmult(relu1, w2_const, vec![1, 40], 1);
    // Rounding division: (mm2 + 64) / 128
    let mm2_rounded = b.poly(PolyOp::Add, mm2, rb40_const, vec![1, 40], 1);
    let mm2_rescaled = b.div(128, mm2_rounded, vec![1, 40], 1);

    let mut b2 = Tensor::new(Some(B2), &[1, 40]).unwrap();
    b2.set_scale(SCALE);
    let b2_const = b.const_tensor(b2, vec![1, 40], 1);
    let biased2 = b.poly(PolyOp::Add, mm2_rescaled, b2_const, vec![1, 40], 1);

    let relu2 = b.relu(biased2, vec![1, 40], 1);

    // Layer 3 (output): [1,40] x [4,40] -> [1,4]
    let mut w3 = Tensor::new(Some(W3), &[4, 40]).unwrap();
    w3.set_scale(SCALE);
    let w3_const = b.const_tensor(w3, vec![4, 40], 1);

    let mm3 = b.matmult(relu2, w3_const, vec![1, 4], 1);
    // Rounding division: (mm3 + 64) / 128
    let mm3_rounded = b.poly(PolyOp::Add, mm3, rb4_const, vec![1, 4], 1);
    let mm3_rescaled = b.div(128, mm3_rounded, vec![1, 4], 1);

    let mut b3 = Tensor::new(Some(B3), &[1, 4]).unwrap();
    b3.set_scale(SCALE);
    let b3_const = b.const_tensor(b3, vec![1, 4], 1);
    let output = b.poly(PolyOp::Add, mm3_rescaled, b3_const, vec![1, 4], 1);

    b.take(vec![input.0], vec![output])
}

